
public class ForLoop_Summer
	{

		public static void main(String[] args)
			{
				int number = 0;
				for (int i = 0; i < 10 ; i++)
					{
						number += 1;
						System.out.println(number);
					}
				
				//For Loop Block Test
				//System.out.println("");
				
				
				for (int i = 10; i > 0 ; i--)
					{
						
						System.out.println(number);
						number -= 1;
					}

			}

	}
